class MathOperations:
    def __init__(self):
        """Initializes the MathOperations class."""
        pass  # No initialization needed for this example

    @staticmethod
    def func_add(num1, num2):
        """Adds two numbers."""
        return num1 + num2

    @staticmethod
    def func_subtract(num1, num2):
        """Subtracts the second number from the first."""
        return num1 - num2

    @staticmethod
    def func_multiply(num1, num2):
        """Multiplies two numbers."""
        return num1 * num2